FOREIGN KEY (CRAM_ID) REFERENCES CRAM_SCHOOL ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE SALARY_HIST (
  TEAC_ID      CHAR(6)  NULL,
  SALARY_START_DATE  NUMERIC(6) NOT NULL,
  SALATY_YEAR   NUMERIC(4) NULL,
  SALATY_MONTH   NUMERIC(2) NULL,
