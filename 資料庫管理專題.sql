CREATE TABLE TEACHER (
  TEAC_ID      CHAR(6)  NOT NULL,
  TEAC_FNAME    VARCHAR(20) NULL,
  TEAC_LNAME    VARCHAR(25) NOT NULL,
  TEAC_PHONE    VARCHAR(25) NOT NULL,
