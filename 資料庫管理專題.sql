CREATE TABLE STUDENT (
  STU_ID      CHAR(6)  NOT NULL,
  STU_FNAME    VARCHAR(20) NULL,
  STU_LNAME    VARCHAR(25) NOT NULL,
  STU_PHONE    VARCHAR(25) NOT NULL,
  STU_SCHOOL    VARCHAR(20) NULL,
  STU_GRADE    NUMERIC(5)         NOT NULL,
  STU_ADDRESS    VARCHAR(100) NOT NULL,
  CRAM_ID       CHAR(10),
  PRIMARY KEY (STU_ID),
  FOREIGN KEY (CRAM_ID) REFERENCES CRAM_SCHOOL ON UPDATE CASCADE ON DELETE CASCADE);

CREATE TABLE TUTION_HIST (
